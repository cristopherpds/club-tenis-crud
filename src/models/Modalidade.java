package models;

public class Modalidade {
  int idmodalidade;
  String modalidade_nome;
  int modalidade_vagas;
  
  public int getIdmodalidade() {
    return idmodalidade;
  }
  public void setIdmodalidade(int idmodalidade) {
    this.idmodalidade = idmodalidade;
  }
  public String getModalidade_nome() {
    return modalidade_nome;
  }
  public void setModalidade_nome(String modalidade_nome) {
    this.modalidade_nome = modalidade_nome;
  }
  public int getModalidade_vagas() {
    return modalidade_vagas;
  }
  public void setModalidade_vagas(int modalidade_vagas) {
    this.modalidade_vagas = modalidade_vagas;
  }
  
}
